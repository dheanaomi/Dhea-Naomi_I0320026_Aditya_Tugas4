y = 11

# a. 4 | 11
a = x | y
print("Nilai a =" ,a, "binernya adalah", bin(x|y))

# b. 4 >> 11
b = x >> y
print("Nilai b =" ,b, "binernya adalah", bin(x>>y))

#c. 4 ^ 11
c = x ^ y
print("Nilai c =" ,c, "binernya adalah", bin(x^y))

#d. ~4
d= ~x
print("Nilai d =" ,d, "binernya adalah", bin(~x))

#e. 11 & 4
e = y & x
